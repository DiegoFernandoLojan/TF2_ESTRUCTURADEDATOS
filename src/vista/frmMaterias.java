    private void mensaje(String data) {
        StringTokenizer st = new StringTokenizer(data, ",");
        String co = st.nextToken();
        String n = st.nextToken();

        String datos = "DETALLES DE DATO ELIMINADO:\n"
                + "CODIGO :" + co + "\n"
                + "NOMBRE  :" + n + "\n";
        JOptionPane.showMessageDialog(this, datos);
    }

    void vaciartabla() {
        int filas = tablaMaterias.getRowCount();
        for (int i = 0; i < filas; i++) {
            modelo.removeRow(0);
        }
    }

    void verdatos() {
        String cod, car, carre;
        Nodo1 aux = lista.frente;
        vaciartabla();
        num = 0;
        while (aux != null) {
            cod = aux.mat.getCodigo_materia();
            car = aux.mat.getNombre_materia();
            carre = aux.mat.getNombre_carrera();
            num++;
            Object[] fila = {num, cod, car, carre};
            modelo.addRow(fila);
            aux = aux.sig;
        }
    }

