    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        String cod = txtcodigo.getText().toUpperCase();
        String car = txtnombreMateria.getText().toUpperCase();
        String mat = cboCarrera.getSelectedItem().toString();

        lista.pFound2 = lista.buscar2(lista.frente, car);
        if (cod.isEmpty() || car.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Â¡RELLENE TODOS LOS CAMPOS!", "ALERTA", JOptionPane.WARNING_MESSAGE);
        } else {
            lista.pFound = lista.buscar(lista.frente, cod);
            if (lista.pFound != null) {
                lista.pFound = lista.buscar2(lista.frente, car);
                if (lista.pFound != null) {
                    JOptionPane.showMessageDialog(this, "Â¡EL CODIGO Y MATERIA YA REGISTRADOS!", "ALERTA", JOptionPane.WARNING_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "Â¡CODIGO YA REGISTRADO, INGRESE OTRO!", "ALERTA", JOptionPane.WARNING_MESSAGE);
                }
            } else if (lista.pFound2 != null) {
                JOptionPane.showMessageDialog(this, "Â¡LA MATERIA YA ESTA REGISTRADA, INGRESE OTRA!", "ALERTA", JOptionPane.WARNING_MESSAGE);
            } else {
                lista.encolar(new materias(new Object[]{cod, car, mat}));
                try {
                    StringBuffer materias = new StringBuffer(txtnombreMateria.getText().trim() + "\t");
                    gestorMaterias.EscribeTxtMateria(materias);
                } catch (Exception e) {
                }
                verdatos();
                gestor.GrabarArchivo(FileName, lista);
            }
        }
    }//GEN-LAST:event_btnguardarActionPerformed

    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        // TODO add your handling code here:
        String cod = txtcodigo.getText();
        if (cod.equalsIgnoreCase("")) {
            JOptionPane.showMessageDialog(this, "INGRESE UN CODIGO EN EL CAMPO CODIGO", "INFORMACION", JOptionPane.INFORMATION_MESSAGE);
        } else {
            lista.pFound = lista.buscar(lista.frente, cod);
            if (lista.pFound != null) {
                txtnombreMateria.setText(lista.pFound.mat.getNombre_materia());
            } else {
                JOptionPane.showMessageDialog(this, "EL CODIGO: " + cod + " NO ESTA EN LA COLA", "ALERTA", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnbuscarActionPerformed

